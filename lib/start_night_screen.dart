// ─── Entry point ──────────────────────────────────────────────────────────────
class StartNightScreen extends StatefulWidget {
  const StartNightScreen({super.key});

  @override
  State<StartNightScreen> createState() => _StartNightScreenState();
}

class _StartNightScreenState extends State<StartNightScreen> {
  final PageController _pageController = PageController();
  int _currentPage = 0;

  // Shared state across steps
  String nightName = '';
  double numBars = 6;
  String vibe = 'Competitive';
  String difficulty = 'Normal';
  bool challengesOn = true;
  bool autoGenerated = true;
  String scoringMode = 'Chaos';
  CityInfo? selectedCity;

  void _next() {
    if (_currentPage < 3) {
      _pageController.nextPage(
        duration: const Duration(milliseconds: 350),
        curve: Curves.easeInOut,
      );
    }
  }

  void _back() {
    if (_currentPage == 0) {
      Navigator.pop(context);
    } else {
      _pageController.previousPage(
        duration: const Duration(milliseconds: 350),
        curve: Curves.easeInOut,
      );
    }
  }

  @override
  void dispose() {
    _pageController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: const Color(0xFF060608),
      body: Stack(
        children: [
          // Background — same style as home
          Container(
            decoration: const BoxDecoration(
              gradient: LinearGradient(
                begin: Alignment.topCenter,
                end: Alignment.bottomCenter,
                colors: [Color(0xFF0B1A14), Color(0xFF060608)],
              ),
            ),
          ),
          const _NightBokeh(),
          Container(
            decoration: const BoxDecoration(
              gradient: RadialGradient(
                center: Alignment(0.0, 0.5),
                radius: 0.7,
                colors: [Color(0x1E7B2CBF), Colors.transparent],
              ),
            ),
          ),

          // Pages
          SafeArea(
            child: Column(
              children: [
                Expanded(
                  child: PageView(
                    controller: _pageController,
                    physics: const NeverScrollableScrollPhysics(),
                    onPageChanged: (i) => setState(() => _currentPage = i),
                    children: [
                      _Step1(
                        nightName: nightName,
                        numBars: numBars,
                        vibe: vibe,
                        onNameChanged: (v) => setState(() => nightName = v),
                        onBarsChanged: (v) => setState(() => numBars = v),
                        onVibeChanged: (v) => setState(() => vibe = v),
                        onBack: _back,
                        onNext: _next,
                      ),
                      _Step2(
                        onBack: _back,
                        onNext: _next,
                        selectedCity: selectedCity,
                        onCityChanged: (c) => setState(() => selectedCity = c),
                      ),
                      _Step3(
                        onBack: _back,
                        onNext: _next,
                        numBars: numBars.round(),
                        selectedCity: selectedCity,
                      ),
                      _Step4(
                        difficulty: difficulty,
                        challengesOn: challengesOn,
                        autoGenerated: autoGenerated,
                        scoringMode: scoringMode,
                        onDifficultyChanged: (v) => setState(() => difficulty = v),
                        onChallengesChanged: (v) => setState(() => challengesOn = v),
                        onAutoGenChanged: (v) => setState(() => autoGenerated = v),
                        onScoringChanged: (v) => setState(() => scoringMode = v),
                        onBack: _back,
                        onStart: () => Navigator.pushReplacement(
                          context,
                          MaterialPageRoute(builder: (_) => ActiveHoleScreen(numHoles: numBars.round())),
                        ),
                      ),
                    ],
                  ),
                ),

                // Page dots
                Padding(
                  padding: const EdgeInsets.only(bottom: 20),
                  child: Row(
                    mainAxisAlignment: MainAxisAlignment.center,
                    children: List.generate(4, (i) {
                      final active = i == _currentPage;
                      return AnimatedContainer(
                        duration: const Duration(milliseconds: 250),
                        margin: const EdgeInsets.symmetric(horizontal: 4),
                        width: active ? 18 : 6,
                        height: 6,
                        decoration: BoxDecoration(
                          borderRadius: BorderRadius.circular(3),
                          color: active
                              ? const Color(0xFF7CB342)
                              : const Color(0xFF333333),
                        ),
                      );
                    }),
                  ),
                ),
              ],
            ),
          ),
        ],
      ),
    );
  }
}

