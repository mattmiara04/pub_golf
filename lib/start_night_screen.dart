// ─── Step 1: Create Your Night — staggered entrance animation ─────────────────
class _Step1 extends StatefulWidget {
  const _Step1({
    required this.nightName, required this.numBars, required this.vibe,
    required this.onNameChanged, required this.onBarsChanged, required this.onVibeChanged,
    required this.onBack, required this.onNext,
  });

  final String nightName, vibe;
  final double numBars;
  final ValueChanged<String> onNameChanged, onVibeChanged;
  final ValueChanged<double> onBarsChanged;
  final VoidCallback onBack, onNext;

  @override
  State<_Step1> createState() => _Step1State();
}

class _Step1State extends State<_Step1> with SingleTickerProviderStateMixin {
  late final AnimationController _ctrl;

  // Each section: fade + upward slide
  late final Animation<double> _headerOpacity;
  late final Animation<Offset> _headerOffset;
  late final Animation<double> _nameOpacity;
  late final Animation<Offset> _nameOffset;
  late final Animation<double> _barsOpacity;
  late final Animation<Offset> _barsOffset;
  late final Animation<double> _vibeOpacity;
  late final Animation<Offset> _vibeOffset;
  late final Animation<double> _ctaOpacity;
  late final Animation<Offset> _ctaOffset;

  static Animation<double> _fadeInterval(
      AnimationController c, double start, double end) {
    return CurvedAnimation(
        parent: c, curve: Interval(start, end, curve: Curves.easeOut));
  }

  static Animation<Offset> _slideInterval(
      AnimationController c, double start, double end,
      {bool fromBelow = false}) {
    return Tween<Offset>(
      begin: Offset(0, fromBelow ? 0.08 : -0.12),
      end: Offset.zero,
    ).animate(CurvedAnimation(
        parent: c, curve: Interval(start, end, curve: Curves.easeOut)));
  }

  @override
  void initState() {
    super.initState();
    _ctrl = AnimationController(
        vsync: this, duration: const Duration(milliseconds: 680));

    // Stagger: header → name → bars → vibe → cta
    _headerOpacity = _fadeInterval(_ctrl, 0.00, 0.44);
    _headerOffset  = _slideInterval(_ctrl, 0.00, 0.44);
    _nameOpacity   = _fadeInterval(_ctrl, 0.12, 0.56);
    _nameOffset    = _slideInterval(_ctrl, 0.12, 0.56);
    _barsOpacity   = _fadeInterval(_ctrl, 0.26, 0.70);
    _barsOffset    = _slideInterval(_ctrl, 0.26, 0.70);
    _vibeOpacity   = _fadeInterval(_ctrl, 0.40, 0.84);
    _vibeOffset    = _slideInterval(_ctrl, 0.40, 0.84);
    _ctaOpacity    = _fadeInterval(_ctrl, 0.56, 1.00);
    _ctaOffset     = _slideInterval(_ctrl, 0.56, 1.00, fromBelow: true);

    _ctrl.forward();
  }

  @override
  void dispose() {
    _ctrl.dispose();
    super.dispose();
  }

  Widget _animated(
      Animation<double> fade, Animation<Offset> slide, Widget child) {
    return FadeTransition(
      opacity: fade,
      child: SlideTransition(position: slide, child: child),
    );
  }

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.symmetric(horizontal: 20),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          // Header
          _animated(
            _headerOpacity, _headerOffset,
            _StepHeader(
              title: 'Create Your Night',
              subtitle: 'Night Name · Optional',
              onBack: widget.onBack,
            ),
          ),
          const SizedBox(height: 16),

          // Night Name section
          _animated(
            _nameOpacity, _nameOffset,
            Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                const Text('Night Name',
                    style: TextStyle(color: Colors.white, fontSize: 15, fontWeight: FontWeight.w600)),
                const SizedBox(height: 8),
                _DarkField(
                  hint: "John's Birthday Run",
                  initialValue: widget.nightName,
                  onChanged: widget.onNameChanged,
                ),
                const SizedBox(height: 6),
                const Text('Name your pub crawl',
                    style: TextStyle(color: Color(0xFF555555), fontSize: 12)),
              ],
            ),
          ),

          const SizedBox(height: 28),

          // Bars section
          _animated(
            _barsOpacity, _barsOffset,
            Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                const Text('Number of Bars',
                    style: TextStyle(color: Colors.white, fontSize: 15, fontWeight: FontWeight.w600)),
                const SizedBox(height: 12),
                _BarsSlider(value: widget.numBars, onChanged: widget.onBarsChanged),
              ],
            ),
          ),

          const SizedBox(height: 28),

          // Vibe section
          _animated(
            _vibeOpacity, _vibeOffset,
            Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                const Text('Vibe',
                    style: TextStyle(color: Colors.white, fontSize: 15, fontWeight: FontWeight.w600)),
                const SizedBox(height: 10),
                _VibePicker(selected: widget.vibe, onChanged: widget.onVibeChanged),
              ],
            ),
          ),

          const Spacer(),

          // CTA — slides up from below
          _animated(
            _ctaOpacity, _ctaOffset,
            _GreenButton(label: 'Continue', onTap: widget.onNext),
          ),
          const SizedBox(height: 16),
        ],
      ),
    );
  }
}

// ─── Bar count slider — haptic on each integer step ───────────────────────────
class _BarsSlider extends StatelessWidget {
  const _BarsSlider({required this.value, required this.onChanged});
  final double value;
  final ValueChanged<double> onChanged;

  @override
  Widget build(BuildContext context) {
    return Column(
      children: [
        SliderTheme(
          data: SliderTheme.of(context).copyWith(
            trackHeight: 4,
            activeTrackColor: const Color(0xFF7CB342),
            inactiveTrackColor: const Color(0xFF2A2A3A),
            thumbColor: Colors.white,
            thumbShape: _BubbleThumbShape(value.round()),
            overlayColor: const Color(0x227CB342),
          ),
          child: Slider(
            value: value,
            min: 3,
            max: 9,
            divisions: 6,
            onChanged: (v) {
              if (v.round() != value.round()) {
                HapticFeedback.lightImpact();
              }
              onChanged(v);
            },
          ),
        ),
        Padding(
          padding: const EdgeInsets.symmetric(horizontal: 4),
          child: Row(
            mainAxisAlignment: MainAxisAlignment.spaceBetween,
            children: const [
              Text('3', style: TextStyle(color: Color(0xFF555555), fontSize: 12)),
              Text('9', style: TextStyle(color: Color(0xFF555555), fontSize: 12)),
            ],
          ),
        ),
      ],
    );
  }
}

class _BubbleThumbShape extends SliderComponentShape {
  final int value;
  const _BubbleThumbShape(this.value);

  @override
  Size getPreferredSize(bool isEnabled, bool isDiscrete) => const Size(28, 28);

  @override
  void paint(PaintingContext context, Offset center, {required Animation<double> activationAnimation,
    required Animation<double> enableAnimation, required bool isDiscrete,
    required TextPainter labelPainter, required RenderBox parentBox,
    required SliderThemeData sliderTheme, required TextDirection textDirection,
    required double value, required double textScaleFactor, required Size sizeWithOverflow}) {
    final canvas = context.canvas;
    canvas.drawCircle(center, 14, Paint()..color = const Color(0xFF7CB342));
    canvas.drawCircle(center, 14, Paint()..color = Colors.white..style = PaintingStyle.stroke..strokeWidth = 2);
    final tp = TextPainter(
      text: TextSpan(text: this.value.toString(),
          style: const TextStyle(color: Colors.white, fontSize: 11, fontWeight: FontWeight.bold)),
      textDirection: TextDirection.ltr,
    )..layout();
    tp.paint(canvas, center - Offset(tp.width / 2, tp.height / 2));
  }
}

// ─── Vibe picker — scale bounce + haptic on selection ─────────────────────────
class _VibePicker extends StatelessWidget {
  const _VibePicker({required this.selected, required this.onChanged});
  final String selected;
  final ValueChanged<String> onChanged;

  @override
  Widget build(BuildContext context) {
    final options = ['Chill', 'Competitive', '😊'];
    return Row(
      children: options.map((o) {
        final isSelected = o == selected;
        return Padding(
          padding: const EdgeInsets.only(right: 10),
          child: GestureDetector(
            onTap: () {
              HapticFeedback.lightImpact();
              onChanged(o);
            },
            child: AnimatedScale(
              scale: isSelected ? 1.05 : 1.0,
              duration: const Duration(milliseconds: 220),
              curve: Curves.easeOut,
              child: AnimatedContainer(
                duration: const Duration(milliseconds: 200),
                padding: const EdgeInsets.symmetric(horizontal: 20, vertical: 10),
                decoration: BoxDecoration(
                  color: isSelected ? const Color(0xFF7B2CBF) : const Color(0xFF111118),
                  borderRadius: BorderRadius.circular(22),
                  border: Border.all(
                    color: isSelected ? const Color(0xFF9D4EDD) : const Color(0xFF2A2A3A),
                    width: 1.5,
                  ),
                  boxShadow: isSelected ? const [
                    BoxShadow(color: Color(0x559D4EDD), blurRadius: 16, spreadRadius: 2),
                  ] : null,
                ),
                child: Text(
                  o,
                  style: TextStyle(
                    color: isSelected ? Colors.white : const Color(0xFF777777),
                    fontWeight: isSelected ? FontWeight.bold : FontWeight.normal,
                    fontSize: 14,
                  ),
                ),
              ),
            ),
          ),
        );
      }).toList(),
    );
  }
}

