// ─── Night map painter ────────────────────────────────────────────────────────
class _NightMapPainter extends CustomPainter {
  @override
  void paint(Canvas canvas, Size size) {
    final w = size.width;
    final h = size.height;

    // Dark city base
    canvas.drawRect(Rect.fromLTWH(0, 0, w, h),
        Paint()..color = const Color(0xFF080C10));

    // City grid — faint amber street lines
    final roadPaint = Paint()
      ..color = const Color(0x22C87010)
      ..strokeWidth = 1;
    for (double x = 0; x < w; x += w / 12) {
      canvas.drawLine(Offset(x, 0), Offset(x, h), roadPaint);
    }
    for (double y = 0; y < h; y += h / 16) {
      canvas.drawLine(Offset(0, y), Offset(w, y), roadPaint);
    }

    // Warm bokeh city lights
    final blobs = [
      [0.6, 0.15, 60.0, const Color(0x18C87818)],
      [0.75, 0.10, 40.0, const Color(0x16D08010)],
      [0.30, 0.25, 50.0, const Color(0x14B06010)],
      [0.85, 0.30, 35.0, const Color(0x12A05010)],
      [0.50, 0.08, 30.0, const Color(0x18E09018)],
    ];
    for (final b in blobs) {
      canvas.drawCircle(
        Offset((b[0] as double) * w, (b[1] as double) * h),
        b[2] as double,
        Paint()
          ..color = b[3] as Color
          ..maskFilter = MaskFilter.blur(BlurStyle.normal, (b[2] as double) * 0.7),
      );
    }

    // Glowing dotted route path
    final routePoints = [
      Offset(w * 0.50, h * 0.88),
      Offset(w * 0.35, h * 0.72),
      Offset(w * 0.55, h * 0.58),
      Offset(w * 0.30, h * 0.44),
      Offset(w * 0.60, h * 0.30),
      Offset(w * 0.45, h * 0.16),
    ];

    // Glow under path
    final glowPaint = Paint()
      ..color = const Color(0x557CB342)
      ..strokeWidth = 8
      ..style = PaintingStyle.stroke
      ..maskFilter = const MaskFilter.blur(BlurStyle.normal, 8)
      ..strokeCap = StrokeCap.round;
    for (int i = 0; i < routePoints.length - 1; i++) {
      canvas.drawLine(routePoints[i], routePoints[i + 1], glowPaint);
    }

    // Dotted route line
    final dotPaint = Paint()
      ..color = const Color(0xFF7CB342)
      ..strokeWidth = 2
      ..style = PaintingStyle.stroke
      ..strokeCap = StrokeCap.round;
    for (int i = 0; i < routePoints.length - 1; i++) {
      _drawDottedLine(canvas, routePoints[i], routePoints[i + 1], dotPaint);
    }

    // Pin colors
    final pinColors = [
      const Color(0xFFF2C94C),
      const Color(0xFFEB5757),
      const Color(0xFF7CB342),
      const Color(0xFF9D4EDD),
      const Color(0xFFEB5757),
      const Color(0xFFF2C94C),
    ];

    for (int i = 0; i < routePoints.length; i++) {
      _drawPin(canvas, routePoints[i], pinColors[i]);
    }
  }

  void _drawDottedLine(Canvas canvas, Offset start, Offset end, Paint paint) {
    final dx = end.dx - start.dx;
    final dy = end.dy - start.dy;
    final dist = math.sqrt(dx * dx + dy * dy);
    final steps = (dist / 10).floor();
    for (int i = 0; i < steps; i += 2) {
      final t0 = i / steps;
      final t1 = (i + 1) / steps;
      canvas.drawLine(
        Offset(start.dx + dx * t0, start.dy + dy * t0),
        Offset(start.dx + dx * t1, start.dy + dy * t1),
        paint,
      );
    }
  }

  void _drawPin(Canvas canvas, Offset pos, Color color) {
    canvas.drawCircle(pos, 14, Paint()
      ..color = color.withValues(alpha: 0.3)
      ..maskFilter = const MaskFilter.blur(BlurStyle.normal, 10));
    canvas.drawCircle(pos, 9, Paint()..color = color);
    canvas.drawCircle(pos, 9, Paint()
      ..color = Colors.white.withValues(alpha: 0.3)
      ..style = PaintingStyle.stroke
      ..strokeWidth = 1.5);
    canvas.drawCircle(pos, 3, Paint()..color = Colors.white);
  }

  @override
  bool shouldRepaint(_NightMapPainter _) => false;
}

